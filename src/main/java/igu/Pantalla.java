    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
     
        String valortxt = jTextField1.getText();
        double valor = Double.parseDouble(valortxt);
        double resultado = valor; //falta modificar
        
        int divInit = jComboBox1.getSelectedIndex();
        int divFin = jComboBox2.getSelectedIndex();
        
        Intercambio intercambio = new Intercambio();
        intercambio.setValor(valor);
        intercambio.setDivFin(divFin);
        intercambio.setDivInit(divInit);
        
        if (esNum(jTextField1.getText().trim())){
            JOptionPane.showMessageDialog(null, "El monto convertido es: " + resultado);
        } else {
            JOptionPane.showMessageDialog(null, "No se permite ingresar digitos que no sean numeros");
        }
        jTextField1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        char caracter = evt.getKeyChar();

        if (((caracter < '0' || caracter > '9'))
            && (caracter !=  KeyEvent.VK_BACK_SPACE)
            && (caracter != '.' )){
            evt.consume();
        }
    }//GEN-LAST:event_jTextField1KeyTyped

